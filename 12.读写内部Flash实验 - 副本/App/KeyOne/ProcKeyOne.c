/*********************************************************************************************************
* 函数名称：ProcKeyDownKey3
* 函数功能：处理按键按下的事件，即按键按下的响应函数 
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2018年01月01日
* 注    意：
*********************************************************************************************************/
void  ProcKeyDownKey3(void)
{
  //printf("KEY3 PUSH DOWN\r\n");   //打印按键状态
	i16 i;
	i16 success = 1;
	u32 arr1[MAX_WR_LEN];
	u32 arr2[MAX_WR_LEN];
	printf("Read Flash\r\n");
	
	STM32FlashReadWord(FLASH_START_ADDR1,arr1,BUF_SIZE/4);
	STM32FlashReadWord(FLASH_START_ADDR2,arr2,BUF_SIZE/4);
	
	for (i = 0; i < BUF_SIZE/4; i++) {
    if (arr1[i] != arr2[i]) {
        success = 0;
        break;
    }
	}

	if (success) {
		OLEDShowString(32,32,"Success!");
		printf("Success!\r\n");
	} else {
		OLEDShowString(32,32,"Failure!");
		printf("Failure!\r\n");
	}
	OLEDRefreshGRAM();
	
	for(i = 0;i<BUF_SIZE/4;i++)
	{
		printf("%08x",arr1[i]);
	}
	printf("\r\n");
		for(i = 0;i<BUF_SIZE/4;i++)
	{
		printf("%08x",arr2[i]);
	}
	printf("\r\n");
	
}

