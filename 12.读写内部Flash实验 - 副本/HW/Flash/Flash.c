//大容量芯片为2KB一个PAGE
#define FLASH_PAGE_SIZE ((u16)0x0800)//十进制为2048
/*********************************************************************************************************
*                                              枚举结构体定义
*********************************************************************************************************/

/*********************************************************************************************************
*                                              内部变量
*********************************************************************************************************/
 static u32 s_arrFlashBuf[FLASH_PAGE_SIZE/4];//最多是2KB
 
/*********************************************************************************************************
*                                              内部函数声明
*********************************************************************************************************/
static u32 ReadWord(const u32 addr);//读取指定地址的字（字，即32位）
//不检查地写入字（字，即32位）格式的数据
static void WriteWordNoCheck(const u32 startAddr, u32 *pBuf,u16 numToWrite);
/*********************************************************************************************************
*                                              内部函数实现
*********************************************************************************************************/
/*********************************************************************************************************
* 函数名称：ReadWord
* 函数功能：
* 输入参数：
* 输出参数：
* 返 回 值：
* 创建日期：2018年01月01日
* 注    意：
*********************************************************************************************************/
static u32 ReadWord(const u32 addr)
{
	return *(vu32*)addr;
}
/*********************************************************************************************************
* 函数名称：WriteWordNoCheck
* 函数功能：
* 输入参数：
* 输出参数：
* 返 回 值：
* 创建日期：2018年01月01日
* 注    意：
*********************************************************************************************************/
static void WriteWordNoCheck(const u32 startAddr, u32 *pBuf,u16 numToWrite)
{
	u16 i;
	u32 addr;
	addr = startAddr;
	for(i = 0;i<numToWrite;i++)
	{
		FLASH_ProgramWord(addr,pBuf[i]);
		addr += 4;//由于字是32位，因此地址增加4
	}
}

