/*********************************************************************************************************
* 函数名称：ProcKeyDownKey1
* 函数功能：处理按键按下的事件，即按键按下的响应函数 
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2018年01月01日
* 注    意：
*********************************************************************************************************/
void  ProcKeyDownKey1(void)
{  
  //printf("KEY1 PUSH DOWN\r\n");  //打印按键状态
	STM32FlashWriteWord(FLASH_START_ADDR1,(u32*)BUF0_U32,BUF_SIZE/4);
	printf("Write Flash:0xFFFFFFFF,0xFFFFFFFF\r\n");
}

