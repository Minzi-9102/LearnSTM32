static  void  ConfigLEDGPIO(void);  //配置LED的GPIO

/*********************************************************************************************************
*                                              内部函数实现
*********************************************************************************************************/
/*********************************************************************************************************
* 函数名称：ConfigLEDGPIO
* 函数功能：配置LED的GPIO 
* 输入参数：void 
* 输出参数：void
* 返 回 值：void
* 创建日期：2018年01月01日
* 注    意：
*********************************************************************************************************/
static  void  ConfigLEDGPIO(void)
{
  GPIO_InitTypeDef GPIO_InitStructure;  //GPIO_InitStructure用于存放GPIO的参数
                                                                     
  //使能RCC相关时钟
  RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOC, ENABLE); //使能GPIOC的时钟
                                                                                                                 
  GPIO_InitStructure.GPIO_Pin   = GPIO_Pin_4;           //设置引脚
  GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;     //设置I/O输出速度
  GPIO_InitStructure.GPIO_Mode  = GPIO_Mode_Out_PP;     //设置模式
  GPIO_Init(GPIOC, &GPIO_InitStructure);                //根据参数初始化LED1的GPIO

  GPIO_WriteBit(GPIOC, GPIO_Pin_4, Bit_SET);            //将LED1默认状态设置为点亮

  GPIO_InitStructure.GPIO_Pin   = GPIO_Pin_5;           //设置引脚
  GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;     //设置I/O输出速度
  GPIO_InitStructure.GPIO_Mode  = GPIO_Mode_Out_PP;     //设置模式
  GPIO_Init(GPIOC, &GPIO_InitStructure);                //根据参数初始化LED2的GPIO

  GPIO_WriteBit(GPIOC, GPIO_Pin_5, Bit_RESET);          //将LED2默认状态设置为熄灭
}

/*********************************************************************************************************
*                                              API函数实现
*********************************************************************************************************/
/*********************************************************************************************************
* 函数名称：InitLED
* 函数功能：初始化LED模块
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2018年01月01日
* 注    意：
*********************************************************************************************************/
void InitLED(void)
{
  ConfigLEDGPIO();  //配置LED的GPIO
}

/*********************************************************************************************************
* 函数名称：LEDFlicker
* 函数功能：LED闪烁函数
* 输入参数：cnt
* 输出参数：void
* 返 回 值：void
* 创建日期：2018年01月01日
* 注    意：LEDFlicker在Proc2msTask中调用，cnt为250时表示每500ms更改一次LED状态
*********************************************************************************************************/
void LEDFlicker(u16 cnt)
{	
	extern bool s_iDirection;
	static i8 state = 0;
  static u16 s_iCnt;  //定义静态变量s_iCnt作为计数器
  
  s_iCnt++; //计数器的计数值加1
  
if(s_iCnt >= cnt)
	{
		s_iCnt = 0;					//重置计数器的计数值为0
		
		switch(state)
		{
			case 0:
				//LED1状态0	
				GPIO_WriteBit(GPIOC,GPIO_Pin_4,(BitAction)0);
				//LED2状态0
				GPIO_WriteBit(GPIOC,GPIO_Pin_5,Bit_RESET);break;
			case 1:
				//LED1状态0	
				GPIO_WriteBit(GPIOC,GPIO_Pin_4,Bit_RESET);
				//LED2状态1
				GPIO_WriteBit(GPIOC,GPIO_Pin_5,Bit_SET);break;
			case 2:
				//LED1状态1	
				GPIO_WriteBit(GPIOC,GPIO_Pin_4,Bit_SET);
				//LED2状态0
				GPIO_WriteBit(GPIOC,GPIO_Pin_5,Bit_RESET);break;
			case 3:
				//LED1状态1	
				GPIO_WriteBit(GPIOC,GPIO_Pin_4,Bit_SET);
				//LED2状态1
				GPIO_WriteBit(GPIOC,GPIO_Pin_5,Bit_SET);break;
			default:break;
		}
		if(s_iDirection)
		{
			state++;
			if(state == 4){state = 0;}
		}
		else
		{
			state--;
			if(state < 0){state = 3;}
		}
	}
}
